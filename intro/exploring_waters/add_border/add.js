function addBorder(picture) {
	let with_border = [];
	let string_size = picture[0].length + 2;
	with_border.push("*".repeat(string_size));
	for(var i = 1; i < picture.length + 1; i++){
		with_border.push("*" + picture[i - 1] + "*");
	}
	with_border.push("*".repeat(string_size));
	return with_border;
}


